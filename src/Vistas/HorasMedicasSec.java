    private void txtRutKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtRutKeyReleased
        if (cboServicio.getSelectedIndex() == 0){
            dbo.filtroHorasRut(tbHoras, txtRut.getText().toUpperCase()+'%');
        }else if(txtRut.getText().isEmpty()){
            dbo.filtroHorasServicio(tbHoras, rescatoNomServicio().getNom_servicio());
        }else if(cboServicio.getSelectedIndex() > 0 ){
            dbo.filtroHorasServicioRut(tbHoras, rescatoNomServicio().getNom_servicio(), txtRut.getText().toUpperCase()+'%');
        }
    }//GEN-LAST:event_txtRutKeyReleased

    private void cboServicioItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboServicioItemStateChanged
        if (cboServicio.getSelectedIndex() == 0){
            dbo.filtroHorasRut(tbHoras, txtRut.getText().toUpperCase()+'%');
        }else if(txtRut.getText().isEmpty()){
            dbo.filtroHorasServicio(tbHoras, rescatoNomServicio().getNom_servicio());
        }else if(cboServicio.getSelectedIndex() > 0 ){
            dbo.filtroHorasServicioRut(tbHoras, rescatoNomServicio().getNom_servicio(), txtRut.getText().toUpperCase()+'%');
        }
    }//GEN-LAST:event_cboServicioItemStateChanged

