    public boolean validarNombre() {
        if (txtNombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese Nombre");
            return false;
        } else {
            return true;
        }
    }

    public Boolean validarInsumo() {
        if (cboTipoInsumo.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Seleccione Tipo Insumo");
            return false;
        } else {
            return true;
        }
    }

    public Boolean validarFamilia() {
        if (cboTipoFamilia.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Seleccione Tipo Familia");
            return false;
        } else {
            return true;
        }
    }

    public Boolean validarPrecio() {
        if (txtPrecio.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese Precio");
            return false;
        } else if (!txtPrecio.getText().matches("[0-9]{1,20}")) {
            JOptionPane.showMessageDialog(null, "Ingrese solo Digitos en Precio");
            return false;
        } else {
            return true;
        }
    }

