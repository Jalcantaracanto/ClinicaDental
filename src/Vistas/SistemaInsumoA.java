    public void cargarComboInsumo() {
        DefaultComboBoxModel modelo = (DefaultComboBoxModel) this.cboTipoInsumo.getModel();
        modelo.removeAllElements();
        ArrayList<TipoInsumo> insu = new ArrayList<TipoInsumo>();
        InsumoBO in = new InsumoBO();
        insu = in.cargarListaTipo();
        modelo.addElement("Seleccione");
        for (TipoInsumo temp : insu) {
            modelo.addElement(temp);
        }
    }

    public void cargarComboFamilia() {
        DefaultComboBoxModel modelo = (DefaultComboBoxModel) this.cboTipoFamilia.getModel();
        modelo.removeAllElements();
        ArrayList<TipoFamilia> fami = new ArrayList<TipoFamilia>();
        InsumoBO fa = new InsumoBO();
        fami = fa.cargarListaFamilia();
        modelo.addElement("Seleccione");
        for (TipoFamilia temp : fami) {
            modelo.addElement(temp);
        }
    }

