    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if (validarNombre() == false || validarInsumo() == false || validarFamilia() == false || validarPrecio() == false) {
        } else {

            Insumo ins = new Insumo();

            ins.setId_insumo(Integer.parseInt(txtID.getText()));
            ins.setNom_insumo(txtNombre.getText().toUpperCase());
            ins.setStock(0);
            ins.setId_tipo_insumo(rescatoIdTipo().getId_tipo_insumo());
            ins.setId_tipo_familia(rescatoIdFamilia().getId_tipo_familia());
            ins.setPrecio_compra(Integer.parseInt(txtPrecio.getText()));

            String mensaje = ibo.agregarInsumo(ins);
            JOptionPane.showMessageDialog(null, mensaje);

            listarInsumo();
            limpiar();
            idMax();
        }       
    }//GEN-LAST:event_btnRegistrarActionPerformed

