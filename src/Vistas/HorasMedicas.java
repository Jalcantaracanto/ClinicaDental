    private void btnTomarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTomarActionPerformed
        if (txtId.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Seleccione la Hora Medica que desea Tomar");
        } else {
            dbo.eliminarHoraMedica(Integer.parseInt(txtId.getText()));
            String mensaje = dbo.eliminarHoraMedica(Integer.parseInt(txtId.getText()));
            JOptionPane.showMessageDialog(null, mensaje);
            limpiar();
            listarClientes();
            
            if (cboServicio.getSelectedIndex() == 0) {
            dbo.filtroHorasRutDoc(tbHoras, txtRut.getText().toUpperCase() + '%', txtDoctor.getText().toUpperCase() + '%');
        } else if (cboServicio.getSelectedIndex() > 0) {
            dbo.filtroHorasServicioRutDoc(tbHoras, txtRut.getText().toUpperCase() + '%', txtDoctor.getText().toUpperCase() + '%', rescatoNomServicio().getNom_servicio());
        }
        }
    }//GEN-LAST:event_btnTomarActionPerformed

