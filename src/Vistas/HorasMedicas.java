    private void txtRutKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtRutKeyReleased
        if (cboServicio.getSelectedIndex() == 0) {
            dbo.filtroHorasRutDoc(tbHoras, txtRut.getText().toUpperCase() + '%', txtDoctor.getText().toUpperCase() + '%');
        } else if (cboServicio.getSelectedIndex() > 0) {
            dbo.filtroHorasServicioRutDoc(tbHoras, txtRut.getText().toUpperCase() + '%', txtDoctor.getText().toUpperCase() + '%', rescatoNomServicio().getNom_servicio());
        }
    }//GEN-LAST:event_txtRutKeyReleased

