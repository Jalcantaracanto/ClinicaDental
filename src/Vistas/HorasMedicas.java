    public void cargarServicio() {
        DefaultComboBoxModel modelo = (DefaultComboBoxModel) this.cboServicio.getModel();
        modelo.removeAllElements();
        ArrayList<Servicio> servicio = new ArrayList<Servicio>();
        ServicioBO sb = new ServicioBO();
        servicio = sb.cargarLista();
        modelo.addElement("Seleccione");
        for (Servicio temp : servicio) {
            modelo.addElement(temp);
        }
    }

    public Servicio rescatoNomServicio() {
        Servicio usu = new Servicio();
        usu = (Servicio) this.cboServicio.getSelectedItem();
        return usu;
    }

